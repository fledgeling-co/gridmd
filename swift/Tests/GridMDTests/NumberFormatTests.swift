final class NumberFormatTests: XCTestCase {
    // (input literal, expected ES output)
    let golden: [(String, String)] = [
        ("0.3", "0.3"), ("-12.5", "-12.5"), ("1e3", "1000"), ("1000", "1000"),
        ("42", "42"), ("84", "84"), ("2003.05", "2003.05"),
        ("0.3771428571428571", "0.3771428571428571"), ("600.915", "600.915"),
        ("584.55", "584.55"), ("234", "234"), ("445", "445"), ("400", "400"),
        ("150", "150"), ("99.5", "99.5"), ("90", "90"), ("0.45", "0.45"),
        ("12.99", "12.99"), ("19.5", "19.5"), ("19.50", "19.5"), ("5.00", "5"),
        ("100.00", "100"), ("45.00", "45"), ("15.00", "15"), ("442.10", "442.1"),
        ("37.77777777777778", "37.77777777777778"), ("0.6104", "0.6104"),
        ("0.5182", "0.5182"), ("0.66", "0.66"), ("0.6600", "0.66"), ("3", "3"),
        ("987.8", "987.8"), ("100", "100"), ("0.6", "0.6"), ("0.30", "0.3"),
        ("0.08", "0.08"), ("0", "0"), ("-0", "0"), ("1", "1"), ("10", "10"),
        ("1000000", "1000000"), ("10000000", "10000000"),
        ("100000000000000000000", "100000000000000000000"),
        ("1e20", "100000000000000000000"), ("1e21", "1e+21"),
        ("1e-6", "0.000001"), ("1e-7", "1e-7"), ("0.0001", "0.0001"),
        ("0.00001", "0.00001"), ("0.000001", "0.000001"), ("0.0000001", "1e-7"),
        ("123456789012345680000", "123456789012345680000"),
        ("1.5e300", "1.5e+300"), ("5e-324", "5e-324"),
        ("1.7976931348623157e308", "1.7976931348623157e+308"),
        ("9007199254740991", "9007199254740991"),
        ("9007199254740993", "9007199254740992"),
        ("0.1", "0.1"), ("0.2", "0.2"), ("1.1", "1.1"), ("2.2", "2.2"),
        ("123.456", "123.456"), ("1000000000000000000000", "1e+21"),
        ("3.141592653589793", "3.141592653589793"),
        ("2.718281828459045", "2.718281828459045"), ("6.022e23", "6.022e+23"),
        ("-3.5e-5", "-0.000035"), ("0.00000000001", "1e-11"),
        ("12345.6789", "12345.6789"),
    ]

    func testGoldenTable() {
        for (input, expected) in golden {
            let value = Double(input)!
            XCTAssertEqual(ESNumber.string(value), expected, "input \(input)")
        }
    }

    func testSpecialValues() {
        XCTAssertEqual(ESNumber.string(Double.nan), "NaN")
        XCTAssertEqual(ESNumber.string(Double.infinity), "Infinity")
        XCTAssertEqual(ESNumber.string(-Double.infinity), "-Infinity")
        XCTAssertEqual(ESNumber.string(-0.0), "0")
    }
}
